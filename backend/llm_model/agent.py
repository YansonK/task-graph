"""LLM Agent with ReAct reasoning and streaming support."""
import os
import logging
import queue
import threading
import asyncio
import dspy
from typing import AsyncGenerator, Dict, Any
from openai import AsyncOpenAI

from .tools import tools, TaskBreakdownSignature
from .config import Config
from .streaming import StreamingLM
from .trajectory_processor import TrajectoryProcessor

# Set up logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


class Agent:
    """AI Agent for task breakdown and graph management using DSPy ReAct."""

    def __init__(self):
        """Initialize the agent with OpenAI configuration."""
        # Validate and get API key
        Config.validate_api_key()
        self.api_key = Config.OPENAI_API_KEY

        logger.info(f"Initializing Agent with OpenAI API key: {self.api_key[:10]}...")

        # Initialize AsyncOpenAI client for streaming
        self.openai_client = AsyncOpenAI(api_key=self.api_key)

        # Configure DSPy with OpenAI GPT-4o-mini
        dspy.configure(lm=dspy.LM(Config.DEFAULT_MODEL, api_key=self.api_key))
        self.react_agent = dspy.ReAct(
            TaskBreakdownSignature,
            tools=list(tools.values()),
            max_iters=Config.MAX_AGENT_ITERATIONS
        )

        # Initialize trajectory processor
        self.trajectory_processor = TrajectoryProcessor()

    def query(self, chat_history: list, graph_data: Dict[str, Any]) -> Any:
        """
        Non-streaming query method for backward compatibility.

        Args:
            chat_history: List of chat messages in OpenAI format
            graph_data: Current graph state with nodes and links

        Returns:
            Agent result with response and updated graph
        """
        # Run ReAct agent
        result = self.react_agent(
            conversation_history=chat_history,
            task_nodes=graph_data
        )

        # Process trajectory and update graph
        self.trajectory_processor.process_trajectory(result, graph_data)

        return result

    async def query_stream(
        self,
        chat_history: list,
        graph_data: Dict[str, Any]
    ) -> AsyncGenerator[Dict[str, Any], None]:
        """
        Streaming query method using DSPy's ReAct agent with real-time streaming.

        This provides intelligent multi-step reasoning with tokens streamed as generated.

        Args:
            chat_history: List of chat messages in OpenAI format
            graph_data: Current graph state with nodes and links

        Yields:
            Dict with 'type' and corresponding data:
            - {'type': 'token', 'content': str} - Text chunk as generated by the model
            - {'type': 'thinking', 'content': str} - Agent's reasoning process
            - {'type': 'graph_update', 'graph_data': dict} - Updated graph
        """
        try:
            # Queue to communicate between sync ReAct and async streaming
            stream_queue = queue.Queue()
            agent_done = threading.Event()
            agent_result = {}
            agent_error = {}

            # Run ReAct agent in background thread with streaming LM
            def run_react_with_streaming():
                """Execute ReAct agent with streaming language model."""
                try:
                    # Create streaming LM instance
                    streaming_lm = StreamingLM(
                        model=Config.DEFAULT_MODEL,
                        api_key=self.api_key,
                        stream_queue=stream_queue
                    )

                    # Run ReAct with streaming LM
                    with dspy.context(lm=streaming_lm):
                        result = self.react_agent(
                            conversation_history=chat_history,
                            task_nodes=graph_data
                        )

                    agent_result['result'] = result
                except Exception as e:
                    agent_error['error'] = e
                    logger.error(f"ReAct agent error: {e}")
                finally:
                    agent_done.set()

            # Start the agent in background thread
            agent_thread = threading.Thread(target=run_react_with_streaming)
            agent_thread.start()

            # Stream tokens from queue as they arrive
            while not agent_done.is_set() or not stream_queue.empty():
                try:
                    # Try to get tokens from queue with short timeout for responsiveness
                    msg_type, content = stream_queue.get(timeout=Config.QUEUE_TIMEOUT)

                    # Yield based on message type
                    yield {
                        'type': msg_type,
                        'content': content
                    }
                except queue.Empty:
                    # No tokens available, minimal wait before checking again
                    await asyncio.sleep(Config.ASYNC_SLEEP)

            # Wait for thread to complete
            agent_thread.join()

            # Check for errors
            if 'error' in agent_error:
                raise agent_error['error']

            # Process tool calls and update graph
            if 'result' in agent_result:
                result = agent_result['result']
                self.trajectory_processor.process_trajectory(result, graph_data)

            # Send final graph update
            yield {
                'type': 'graph_update',
                'graph_data': graph_data
            }

        except Exception as e:
            logger.error(f"Streaming error: {e}")
            yield {
                'type': 'token',
                'content': f"Sorry, I encountered an error: {str(e)}"
            }
